import numpy as np

A = np.array([
    [2.12, 0.42, 1.34, 0.88],
    [0.42, 3.95, 1.87, 0.43],
    [1.34, 1.87, 2.98, 0.46],
    [0.88, 0.43, 0.46, 4.44]
], dtype=float)

b = np.array([11.172, 0.115, 0.009, 9.349], dtype=float)

x = np.array([7.220063842915759, 1.08331070232564, -4.076517192429, 0.9920536317007763], dtype=float)

# Обчислення вектора нев’язки: r = b - A*x
residual = b - np.dot(A, x)

print("Вектор нев’язки:", residual)
